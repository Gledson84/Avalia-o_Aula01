# Levei em consideração que as dimensões estão em centímetros.
base_retangulo = float(input('Informe o comprimento da base: '))
altura_retangulo = float(input('Informe o comprimento da altura: '))
area_retangulo = base_retangulo * altura_retangulo
print(f'A área do retângulo é: {area_retangulo} cm')